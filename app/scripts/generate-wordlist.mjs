import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const txtPath = resolve(__dirname, '../../webapp/data/languages/en/en_5words.txt')
const words = readFileSync(txtPath, 'utf-8')
    .split('\n')
    .map((w) => w.trim().toLowerCase())
    .filter((w) => w.length === 5)

const out = `// Auto-generated by scripts/generate-wordlist.mjs — do not edit
export const validWords = new Set<string>(${JSON.stringify(words)})
`
writeFileSync(resolve(__dirname, '../src/wordList.generated.ts'), out)
console.log(`Word list generated: ${words.length} words`)
